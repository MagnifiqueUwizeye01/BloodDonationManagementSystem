     private void showErrorAndFocus(String message, JComponent field) {
        // Close previous dialog if exists
        if (currentErrorDialog != null) {
            currentErrorDialog.dispose();
        }

        currentErrorDialog = new JDialog(this, "Validation Error", true);
        currentErrorDialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);

        JPanel panel = new JPanel(new BorderLayout());
        panel.add(new JLabel(message), BorderLayout.CENTER);

        JButton okButton = new JButton("OK");
        okButton.addActionListener(e -> {
            currentErrorDialog.dispose();
            currentErrorDialog = null;
            field.requestFocus();
        });

        JPanel buttonPanel = new JPanel();
        buttonPanel.add(okButton);
        panel.add(buttonPanel, BorderLayout.SOUTH);

        currentErrorDialog.add(panel);
        currentErrorDialog.pack();
        currentErrorDialog.setLocationRelativeTo(this);
        currentErrorDialog.setVisible(true);
    }

    
    
    

