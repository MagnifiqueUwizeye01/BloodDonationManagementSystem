    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        // TODO add your handling code here:
   boolean isValid = true;
        String nationalId = nationalIdField.getText().trim();
        String username = usernameField.getText().trim();
        String email = emailField.getText().trim();
        String password = new String(passwordField.getPassword()).trim();
        String phone = phoneField.getText().trim();
        
        // Validate National ID
        if (nationalId.isEmpty()) {
            showErrorAndFocus("National ID is required", nationalIdField);
            isValid = false;
        } else if (!nationalId.matches("\\d+")) {
            showErrorAndFocus("National ID must contain only digits", nationalIdField);
            isValid = false;
        } else if (nationalId.length() != 16) {
            showErrorAndFocus("National ID must be exactly 16 digits", nationalIdField);
            isValid = false;
        }

        // Validate Username
        else if (username.isEmpty()) {
            showErrorAndFocus("Username is required", usernameField);
            isValid = false;
        } else if (username.length() < 4) {
            showErrorAndFocus("Username must be at least 4 characters", usernameField);
            isValid = false;
        } else if (!username.matches("[a-zA-Z]+( [a-zA-Z]+)+")) {
            showErrorAndFocus("Username must contain at least two words with only letters", usernameField);
            isValid = false;
        }

        // Validate Email
        else if (email.isEmpty()) {
            showErrorAndFocus("Email is required", emailField);
            isValid = false;
        } else if (!email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
            showErrorAndFocus("Invalid email format", emailField);
            isValid = false;
        }

        // Validate Password
        else if (password.isEmpty()) {
            showErrorAndFocus("Password is required", passwordField);
            isValid = false;
        } else if (password.length() < 8) {
            showErrorAndFocus("Password must be at least 8 characters", passwordField);
            isValid = false;
        }

        // Validate Phone
        else if (phone.isEmpty()) {
            showErrorAndFocus("Phone number is required", phoneField);
            isValid = false;
        } else if (!phone.matches("^\\+?[0-9]{10,15}$")) {
            showErrorAndFocus("Phone must be 10-15 digits (optional + prefix)", phoneField);
            isValid = false;
        }

        // If any validation fails, stop form submission
        if (!isValid) {
            return; 
        }

        // Proceed with registration if all fields are valid
        try {
            User newUser = new User(nationalId, username, email, password, phone);
            UserDao userDao = new UserDao();
            if (userDao.registerUser(newUser)) {
                JOptionPane.showMessageDialog(this, "Registration successful!", "Success", JOptionPane.INFORMATION_MESSAGE);
                new LoginForm().setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Username or email already exists", "Registration Failed", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Registration Error", JOptionPane.ERROR_MESSAGE);
        }
    
    
    }//GEN-LAST:event_registerButtonActionPerformed

    private void signInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInButtonActionPerformed
        // TODO add your handling code here:
     if (hasUnsavedData()) {
            int option = JOptionPane.showConfirmDialog(
                this,
                "You have unsaved data. Proceed to login?",
                "Confirm Navigation",
                JOptionPane.YES_NO_OPTION
            );
            if (option != JOptionPane.YES_OPTION) return;
        }

        // Navigate to login
        new LoginForm().setVisible(true);
        this.dispose();
    }

    private boolean hasUnsavedData() {
        return !nationalIdField.getText().trim().isEmpty() ||
               !usernameField.getText().trim().isEmpty() ||
               !emailField.getText().trim().isEmpty() ||
               !new String(passwordField.getPassword()).trim().isEmpty() ||
               !phoneField.getText().trim().isEmpty();
                            

    }//GEN-LAST:event_signInButtonActionPerformed

