    public DashboardForm(User user) {
        this.currentUser = user;

        try {
            // Connect to RMI registry
            Registry registry = LocateRegistry.getRegistry("localhost", 5000);
            donorService = (DonorService) registry.lookup("donor");
            donationService = (DonationService) registry.lookup("donation");
            bloodRequestService = (BloodRequestService) registry.lookup("bloodRequest");
            bloodInventoryService = (BloodInventoryService) registry.lookup("bloodInventory");


            System.out.println("[Dashboard] âœ… Connected to DonorService and DonationService.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to connect to RMI services.\n" + e.getMessage(),
                    "RMI Connection Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

        initComponents();
        this.setSize(1700, 1000);
        jPanel2.setPreferredSize(new Dimension(1100, 800));

        cardLayout = new CardLayout();
        jPanel2.setLayout(cardLayout);

        // Pass services to each form
        donorManagementPanel = new DonorManagementForm(currentUser, donorService);
        donationPanel = new DonationForm(currentUser, donorService, donationService);
        bloodRequestPanel = new BloodRequestPanel(currentUser, bloodRequestService);
        bloodInventoryPanel = new BloodInventoryPanel(bloodInventoryService);
        
        

        jPanel2.add(donorManagementPanel, "donorPanel");
        jPanel2.add(donationPanel, "donationPanel");
        jPanel2.add(bloodRequestPanel, "bloodRequestPanel");
        jPanel2.add(bloodInventoryPanel, "BloodInventory");

        welcomeLabel.setText("Welcome, " + currentUser.getUsername());
        setLocationRelativeTo(null);
    }
    
    

